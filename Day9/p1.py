import pandas as pd

p=pd.read_csv("passengers.csv")
f=pd.read_csv("flights.csv")
b=pd.read_csv("bookings.csv")

p_b=pd.merge(p,b,on="PassengerID")
mer=pd.merge(p_b,f,on="FlightID")
print("Complete Reservation Report")
merged=mer.loc[:,["BookingID","PassengerName","City","Airline","Destination","TicketFare"]].sort_values(by="BookingID")
print(merged.to_string(index=False,header=False))
print("\nPassengers Travelling to Dubai")
print(merged.loc[merged["Destination"]=="Dubai",["PassengerName","Airline","TicketFare"]].to_string(index=False, header=False))
print("\nThird and Fourth Reservations")
print(merged.iloc[2:4][["BookingID","PassengerName","Airline","TicketFare"]].to_string(index=False, header=False))
print("\nAirline Revenue Report")
revenue=merged.groupby("Airline")['TicketFare'].sum().sort_values(ascending=False)
for airline,fare in revenue.items():
    print(airline,fare)
airline=revenue.idxmax()
fare=revenue.max()
print("\nHighest Revenue Airline")
print(airline,fare)


